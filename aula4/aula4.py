"""
Tipos de dados
str - string - texto 'Assim' "Assim"
int - inteiro - 123456 0 -10 -10 -50 -60 -15000 1500
float - real/ponto flutuante - 10.50 1.5 -10.10 -50.93 0.0
bool - booleano/lógico - True/False
"""

# String: nome
print('Luan', type('Luan'))

# Idade: int
print(22, type(22))

# ALtura: float
print(1.70, type(1.70))

